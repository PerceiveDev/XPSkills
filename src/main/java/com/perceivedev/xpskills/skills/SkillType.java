package com.perceivedev.xpskills.skills;

/**
 * The type of a skill
 */
public enum SkillType {

    /**
     * Increases the player's attack damage
     */
    ATTACK_DAMAGE,
    /**
     * Increases the player's attack speed
     */
    ATTACK_SPEED,
    /**
     * Increases the player's health
     */
    HEALTH_BOOST,
    /**
     * Increases the player's unarmed (with bare hands) damage
     */
    UNARMED_DAMAGE,
    /**
     * Increases the experience you get
     */
    EXPERIENCE_AMPLIFIER

}
